TextStyle BonaNova({
  double fontSize = 15,
  FontWeight fontWeight = FontWeight.normal,
  Color color = Colors.black,
}) {
  return GoogleFonts.bonaNova(
    fontSize: fontSize,
    fontWeight: fontWeight,
    color: color,
